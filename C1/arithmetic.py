
x = float(27.5)
y = int(2)
addition = x + y
substraction = x - y
multiplication = x * y
trueDivision = x / y
floorDivision = x // y
exponential = x ** y
print(x, ' + ', y, ' = ', addition)
print(x, ' - ', y, ' = ', substraction)
print(x, ' * ', y, ' = ', multiplication)
print(x, ' / ', y, ' = ', trueDivision)
print(x, ' // ', y, ' = ', floorDivision)
print(x, ' ** ', y, ' = ', exponential)
